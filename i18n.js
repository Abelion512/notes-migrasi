export function applyTranslations(lang, toggleElement) {
  const strings = I18N_STRINGS[lang] ?? I18N_STRINGS.id;
  document.documentElement.lang = lang;
  const map = new Map(Object.entries(strings));

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (map.has(key)) {
      el.textContent = map.get(key);
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (map.has(key)) {
      el.setAttribute('placeholder', map.get(key));
    }
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
    const key = el.dataset.i18nAriaLabel;
    if (map.has(key)) {
      el.setAttribute('aria-label', map.get(key));
    }
  });

  document.querySelectorAll('option[data-i18n]').forEach(option => {
    const key = option.dataset.i18n;
    if (map.has(key)) {
      option.textContent = map.get(key);
    }
  });

  if (toggleElement) {
    const toggle = typeof toggleElement === 'string' ? document.querySelector(toggleElement) : toggleElement;
    if (toggle) {
      toggle.textContent = lang === 'id' ? 'EN' : 'ID';
    }
  }

  return map;
}
